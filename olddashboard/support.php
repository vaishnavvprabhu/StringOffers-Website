<script>
function openForm() {
    document.getElementById("LiveChat").style.display = "block";
    scrollToBottom();
}

function closeForm() {
    document.getElementById("LiveChat").style.display = "none";
}

function getSettings() {
    document.getElementById("chat-body").style.display = "none";
    document.getElementById("chat-settings").style.display = "block";
}

function hideSettings() {
    document.getElementById("chat-settings").style.display = "none";
    document.getElementById("chat-body").style.display = "block";
    document.getElementById("start-chat-text").style.display = "none";
    document.getElementById("start-chat-button").innerHTML = "Save Settings";
    scrollToBottom();
}

function scrollToBottom() {
    var objDiv = document.getElementById("chat-conversation");
    objDiv.scrollTop = objDiv.scrollHeight;
}

//Let's detect iOS because display issues. Not used yet, but we'll need it to fix some things, I think.
function iOS() {
    var iDevices = [
        "iPad Simulator",
        "iPhone Simulator",
        "iPod Simulator",
        "iPad",
        "iPhone",
        "iPod"
    ];

    if (!!navigator.platform) {
        while (iDevices.length) {
            if (navigator.platform === iDevices.pop()) {
                return true;
            }
        }
    }
    return false;
}
</script>



